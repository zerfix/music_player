#[derive(Clone, Copy)]
#[derive(Debug)]
pub struct StateInterface {
    pub is_scanning: bool,
    pub current_view: CurrentView,
}

#[derive(Clone, Copy)]
#[derive(Debug)]
pub enum CurrentView {
    Library,
}

impl StateInterface {
    pub fn init() -> StateInterface {
        StateInterface{
            is_scanning: false,
            current_view: CurrentView::Library,
        }
    }
}
